func TestTest_RunsFixtures(t *testing.T) {
	t.Parallel()
	g := New()
	intent := sampleIntent()
	a, err := g.Compile(context.Background(), intent)
	if err != nil {
		t.Fatalf("Compile: %v", err)
	}
	results, err := g.Test(context.Background(), a, intent)
	if err != nil {
		t.Fatalf("Test: %v", err)
	}
	// 1 positive + 2 negative = 3 fixture results
	if len(results) < 3 {
		t.Fatalf("expected >=3 results, got %d", len(results))
	}
	pass := 0
	for _, r := range results {
		if r.Passed {
			pass++
		}
	}
	if pass < 3 {
		t.Errorf("expected all fixtures pass, got %d/%d", pass, len(results))
	}
}

func TestCompile_EnforceMode_BlocksOnFail(t *testing.T) {
	t.Parallel()
	g := New()
	intent := sampleIntent()
	a, err := g.Compile(context.Background(), intent)
	if err != nil {
		t.Fatalf("Compile: %v", err)
	}
	// When severity=error, the playbook must include the enforcement fail task.
	if !strings.Contains(a.Content, "Egide enforcement · fail if any audit task failed") {
		t.Error("expected enforcement fail task when severity=error")
	}
}

func TestCompile_AutoRemediation(t *testing.T) {
	t.Parallel()
	g := New()
	intent := sampleIntent()
	intent.ActionsOnViolation = []string{"audit", "auto_remediate"}
	a, err := g.Compile(context.Background(), intent)
	if err != nil {
		t.Fatalf("Compile: %v", err)
	}
	if !strings.Contains(a.Content, "tags: [remediate, egide]") {
		t.Error("expected remediation tag when auto_remediate action present")
	}
	if !strings.Contains(a.Content, "apply_remediation | default(false) | bool") {
		t.Error("expected remediation gated by apply_remediation var")
	}
}
